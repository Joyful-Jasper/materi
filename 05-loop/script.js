// for (start, stop, step)
// counted loop
for (let i = 1; i <= 5; i++) {
  console.log(i)
}

// while
// mirip seperti if, tapi di ulang
// uncounted loop 
// nanya dulu, kemudian jalan
// let i = 1
while (i <= 5) {
  console.log(i)
  i++
}

// do while
// jalan dulu, kemudian nanya
i = 1
do {
  console.log(i)
  i++
} while (i <= 5)






